<script type="text/javascript">

	new WOW().init();
	
	var slideHeight = $(window).height();
	slideHeight = slideHeight + 100;
	$('#section0').css('height',slideHeight);

	$(window).resize(function(){
		var slideHeight = $(window).height();
		slideHeight = slideHeight + 100;
		$('#section0').css('height',slideHeight);
	});
	
	animacion = setInterval(mostrarBowl, 6500);
	var claseToShow = 1;
	
	function mostrarBowl(){
		
		if(claseToShow > 14){
			claseToShow = 1;
		}
		
		$('#selectedInfo div:first-child img').css('left', '-100px');
		$('#selectedInfo div:first-child img').css('opacity', '0');
		$('#selectedInfo div:last-child img').css('right', '-100px');
		$('#selectedInfo div:last-child img').css('opacity', '0');
		$('.selected').css('display', 'none');
		
		
		$('#selectedInfo').css('display', 'block');
		$('#selectedInfo .selected.selected-' + claseToShow).fadeIn();
		
		$('#lista-ingredientes a').removeClass('active');
		$('#lista-ingredientes a.menu-' + claseToShow).addClass('active');
		
		$("#selectedInfo div:first-child img").animate({
			left: '0',
			opacity: 1
		});
		$("#selectedInfo div:last-child img").animate({
			right: '0',
			opacity: 1
		});
		
		var imagenIng = 'images/bowlPerro/bowl-' + claseToShow + '.png';
		$('#bowl-ingredientes').attr('src', imagenIng);
		
		claseToShow++;
		
	}
	
	$("#lista-ingredientes a").click(function(){
		
		claseToShow = $(this).find('.clase').html();
		
		$('#selectedInfo div:first-child img').css('left', '-100px');
		$('#selectedInfo div:first-child img').css('opacity', '0');
		$('#selectedInfo div:last-child img').css('right', '-100px');
		$('#selectedInfo div:last-child img').css('opacity', '0');
		$('.selected').css('display', 'none');
		
		
		$('#selectedInfo').css('display', 'block');
		$('#selectedInfo .selected.selected-' + claseToShow).fadeIn();
		
		$('#lista-ingredientes a').removeClass('active');
		$('#lista-ingredientes a.menu-' + claseToShow).addClass('active');
		
		$("#selectedInfo div:first-child img").animate({
			left: '0',
			opacity: 1
		});
		$("#selectedInfo div:last-child img").animate({
			right: '0',
			opacity: 1
		});
		
		var imagenIng = 'images/bowlPerro/bowl-' + claseToShow + '.png';
		$('#bowl-ingredientes').attr('src', imagenIng);
		
		claseToShow++;
		
	});


</script>

